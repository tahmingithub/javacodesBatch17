package Class2;

public class E8shorthandOperators {
    public static void main(String[] args) {

        int number=20;
        number=number+10;
        System.out.println(number);

        int number1=10;
        number1+=40;// add 40 more to the value of  THE number //+= called shorthand
        System.out.println(number1);

        int number2=100;// subtract 40 from the value of hte number
        number2-=20;
        System.out.println(number2);

        int number3=10;
        number3*=2;
        System.out.println(number3);




    }
}
