package review5;

public class LoopsRecap {
    public static void main(String[] args) {


        for (int i = 0; i <=5 ; i++) { // initialize means i =1
            // code to repeat
            System.out.println("hello");
        }
        for (int i = 2; i <=10 ; i+=3) {
            System.out.println(i);
        }
    }
}
