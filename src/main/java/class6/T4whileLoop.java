public class T4whileLoop {
    public static void main(String[] args) {
        /*
        print 1 3 5 7 9 11
         */

        int i = 1;// starting point  is 1
        while (i <= 11) {// ending point is 11
            System.out.println(i);
            i++;// steps
            i++;


        }
    }

}
