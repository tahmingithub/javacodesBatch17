package class7;

public class E3NestedLoops {
    public static void main(String[] args) {



    for (int j = 0; j < 3; j++) {    // outer loop it says print 3 line
        for (int i = 2; i <20 ; i=i+2) {    // inner loop says to print from 2-20 even number
            System.out.print(i+" ");
        }
        System.out.println();
    }
}
}
