package class3;

public class E3RelationalOperators {
    public static void main(String[] args) {
        System.out.println(10>15);// is 10  bigger than 15, false
        System.out.println(10==15);// is 10 equal to 15, false
        System.out.println(10!=15);// is 10 not equal to 10, true
        System.out.println(10>=15);// is 10 greater or equal to 15, false
        System.out.println(10<=15);// is 10 less than or equal to 15,  True or false //if one part is true the overall part is true     when we have or , if one part is true , the overall result should be true
//




    }
}
