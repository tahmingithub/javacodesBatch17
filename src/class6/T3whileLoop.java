package class6;

public class T3whileLoop {
    public static void main(String[] args) {
         //  write while loop that prints numbers from 10-20.


        int i=10;
        while(i<=20){
            System.out.print(i+ " ");
            i+=1;//  += means add//  ++ means add one so we can say i++;
        }

    }
}
